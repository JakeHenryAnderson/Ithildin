class LiveDemoEvidenceSummaryError(RuntimeError):
    """Raised when the live-demo evidence summary cannot be built."""


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--json", action="store_true", dest="json_output")
    parser.add_argument("--output", type=Path, default=DEFAULT_OUTPUT)
    parser.add_argument("--skip-probes", action="store_true")
    args = parser.parse_args()
    try:
        report = build_summary(
            repo_root=Path.cwd().resolve(),
            output=args.output,
            probe_endpoints=not args.skip_probes,
        )
    except LiveDemoEvidenceSummaryError as exc:
        print(f"live demo evidence summary failed: {exc}", file=sys.stderr)
        return 1
    if args.json_output:
        print(json.dumps(report, indent=2, sort_keys=True))
    else:
        print(f"Built live-demo evidence summary at {args.output}")
        print(f"status: {'pass' if report['valid'] else 'fail'}")
    return 0 if report["valid"] else 1


def build_summary(
    *,
    repo_root: Path,
    output: Path,
    probe_endpoints: bool = True,
) -> dict[str, Any]:
    _require_project_root(repo_root)
    status = live_demo_status.build_status(
        repo_root=repo_root,
        output=output.parent / "LIVE_DEMO_INDEX.md",
        probe_endpoints=probe_endpoints,
    )
    artifacts = _artifact_report(repo_root)
    report = {
        "valid": bool(status["valid"]),
        "generated_at": datetime.now(UTC).isoformat(),
        "repo_root": str(repo_root),
        "preflight": status["preflight"],
        "endpoints": status["endpoints"],
        "artifacts": artifacts,
        "does_not_prove": [
            "OS isolation",
            "host compromise resistance",
            "SIEM custody",
            "compliance automation",
            "production security",
            "external notarization",
            "activity outside Ithildin-mediated actions",
        ],
    }
    rendered = render_summary(report)
    _assert_secret_free(rendered)
    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text(rendered, encoding="utf-8")
    return report


