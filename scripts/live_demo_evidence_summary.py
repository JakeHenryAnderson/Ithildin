def _artifact_report(repo_root: Path) -> dict[str, dict[str, Any]]:
    return {name: _artifact_entry(repo_root, path) for name, path in ARTIFACTS.items()}


def _artifact_entry(repo_root: Path, path: Path) -> dict[str, Any]:
    full_path = repo_root / path
    sha = _digest(full_path) if full_path.is_file() else None
    return {
        "path": path.as_posix(),
        "exists": full_path.exists(),
        "type": "directory" if full_path.is_dir() else "file" if full_path.is_file() else "missing",
        "sha256": sha,
    }


def _digest(path: Path) -> str:
    digest = hashlib.sha256()
    with path.open("rb") as handle:
        for chunk in iter(lambda: handle.read(1024 * 1024), b""):
            digest.update(chunk)
    return "sha256:" + digest.hexdigest()


def _assert_secret_free(text: str) -> None:
    for marker in FORBIDDEN_MARKERS:
        if marker in text:
            raise LiveDemoEvidenceSummaryError(
                f"summary contains forbidden marker: {marker}"
            )


def _require_project_root(repo_root: Path) -> None:
    missing = [marker.as_posix() for marker in PROJECT_MARKERS if not (repo_root / marker).exists()]
    if missing:
        raise LiveDemoEvidenceSummaryError(
            "must be run from the Ithildin repo root; missing markers: " + ", ".join(missing)
        )


