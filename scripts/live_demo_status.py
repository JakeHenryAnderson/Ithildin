def _require_project_root(repo_root: Path) -> None:
    missing = [marker.as_posix() for marker in PROJECT_MARKERS if not (repo_root / marker).exists()]
    if missing:
        raise LiveDemoStatusError(
            "must be run from the Ithildin repo root; missing markers: " + ", ".join(missing)
        )


def _probe_url(url: str) -> dict[str, Any]:
    try:
        with urllib.request.urlopen(url, timeout=1) as response:
            status = int(response.status)
    except (OSError, urllib.error.URLError) as exc:
        return {"reachable": False, "status_code": None, "safe_error": type(exc).__name__}
    return {"reachable": 200 <= status < 500, "status_code": status, "safe_error": None}


def _skipped() -> dict[str, Any]:
    return {"reachable": False, "status_code": None, "safe_error": "probe_skipped"}


def _compose_status(repo_root: Path) -> dict[str, Any]:
    env_file = ".env" if (repo_root / ".env").exists() else ".env.example"
    command = [
        "docker",
        "compose",
        "--env-file",
        env_file,
        "-f",
        "deploy/docker-compose.yml",
        "ps",
        "--format",
        "json",
    ]
    try:
        result = subprocess.run(
            command,
            cwd=repo_root,
            capture_output=True,
            text=True,
            check=False,
            timeout=COMPOSE_STATUS_TIMEOUT_SECONDS,
        )
    except subprocess.TimeoutExpired:
        return {
            "available": False,
            "running_hint": False,
            "safe_error": "compose status timed out",
        }
    if result.returncode != 0:
        return {
            "available": False,
            "running_hint": False,
            "safe_error": (result.stderr or result.stdout).strip()[:200] or "compose unavailable",
        }
    output = result.stdout.lower()
    return {
        "available": True,
        "running_hint": "ithildin-api" in output and "ithildin-ui" in output,
        "safe_error": None,
    }


def _artifact_status(repo_root: Path) -> dict[str, dict[str, Any]]:
    result: dict[str, dict[str, Any]] = {}
    for name, path in ARTIFACTS:
        full_path = repo_root / path
        result[name] = {
            "path": path.as_posix(),
            "exists": full_path.exists(),
            "is_dir": full_path.is_dir(),
        }
    return result


def _next_actions(
    *,
    preflight: dict[str, Any],
    compose: dict[str, Any],
    api: dict[str, Any],
    ui: dict[str, Any],
) -> list[str]:
    if not preflight["valid"]:
        return ["fix preflight failures before running the live demo"]
    actions = ["run make demo-seed before any demo-flow execution"]
    if not compose["available"] or not compose["running_hint"]:
        actions.append("run make live-demo-environment-diagnostics if Compose is unavailable")
    if not api["reachable"] or not ui["reachable"]:
        actions.append("run make compose-up and make compose-smoke for local API/UI demo")
    actions.extend(
        [
            "run make live-demo-smoke after readiness checks",
            "run make live-demo-packet before handoff",
            "run make compose-down after the Compose demo",
        ]
    )
    return actions


def _print_human(report: dict[str, Any]) -> None:
    print("Ithildin live-demo status")
    print(f"status: {'pass' if report['valid'] else 'fail'}")
    print(f"preflight_valid: {str(report['preflight']['valid']).lower()}")
    print(f"tool_count: {report['preflight']['tool_count']}")
    print(f"compose_available: {str(report['compose']['available']).lower()}")
    print(f"compose_running_hint: {str(report['compose']['running_hint']).lower()}")
    print(f"api_healthz_reachable: {str(report['endpoints']['api_healthz']['reachable']).lower()}")
    print(f"ui_root_reachable: {str(report['endpoints']['ui_root']['reachable']).lower()}")
    print("\nartifacts:")
    for name, entry in report["artifacts"].items():
        print(f"- {name}: {entry['path']} exists={str(entry['exists']).lower()}")
    print("\nnext actions:")
    for action in report["next_actions"]:
        print(f"- {action}")
    print(f"\ncleanup: {report['cleanup_reminder']}")


