class LiveDemoStatusError(RuntimeError):
    """Raised when live-demo status cannot be built."""


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--json", action="store_true", dest="json_output")
    parser.add_argument("--output", type=Path, default=DEFAULT_OUTPUT)
    parser.add_argument("--skip-probes", action="store_true")
    args = parser.parse_args()
    try:
        report = build_status(
            repo_root=Path.cwd().resolve(),
            output=args.output,
            probe_endpoints=not args.skip_probes,
        )
    except LiveDemoStatusError as exc:
        print(f"live demo status failed: {exc}", file=sys.stderr)
        return 1
    if args.json_output:
        print(json.dumps(report, indent=2, sort_keys=True))
    else:
        _print_human(report)
    return 0 if report["valid"] else 1


def build_status(*, repo_root: Path, output: Path, probe_endpoints: bool = True) -> dict[str, Any]:
    _require_project_root(repo_root)
    preflight = live_demo_preflight.build_report(repo_root)
    api_health = _probe_url("http://127.0.0.1:8000/healthz") if probe_endpoints else _skipped()
    ui_health = _probe_url("http://127.0.0.1:5173/") if probe_endpoints else _skipped()
    compose = _compose_status(repo_root)
    artifacts = _artifact_status(repo_root)
    report = {
        "valid": bool(preflight["valid"]),
        "generated_at": datetime.now(UTC).isoformat(),
        "repo_root": str(repo_root),
        "preflight": {
            "valid": preflight["valid"],
            "warnings": preflight["warnings"],
            "failures": preflight["failures"],
            "tool_count": preflight["tool_count"],
            "loopback_ports_valid": preflight["loopback_ports_valid"],
            "docker_socket_mounted": preflight["docker_socket_mounted"],
            "telemetry_enabled": preflight["telemetry_enabled"],
            "http_allowlist_count": preflight["http_allowlist_count"],
        },
        "compose": compose,
        "endpoints": {
            "api_healthz": api_health,
            "ui_root": ui_health,
        },
        "artifacts": artifacts,
        "next_actions": _next_actions(
            preflight=preflight,
            compose=compose,
            api=api_health,
            ui=ui_health,
        ),
        "cleanup_reminder": "run make compose-down after a Compose demo",
        "does_not_do": [
            "start services",
            "stop services",
            "mutate workspaces",
            "call governed tools",
            "read secrets",
            "send network traffic outside localhost probes",
        ],
    }
    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text(render_index(report), encoding="utf-8")
    return report


